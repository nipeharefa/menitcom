     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $news = Auth::user()->News()->get()->map(function($item){
            return [
                'id' => $item->id,
                'title' => $item->title,
                'thumbnail' => $item->thumbnail
            ];
        });
        return response()->json($news, 200);
    }

